// BUCLE FOR...OF

// Sintaxis: for (variable of iterable) { sentencias}
const frutas = ["manzanas", "peras", "uvas", "naranjas", "melocotones",];
for(let fruta of frutas){
    console.log(fruta);
}

// Iterar un string o cadena de texto
const animal = "Elefante";
for(let caracter of animal){
    console.log(caracter);
}