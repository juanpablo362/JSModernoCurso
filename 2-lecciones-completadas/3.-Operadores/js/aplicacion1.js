// OPERADOR DE ASIGNACIÓN

let a = 55;
console.log(a);

// Operador de asignación con suma
let b = 10;
b += 5; // Esto es lo mismo que b + 5
console.log(b);

// Asignación con resta
let c = 80;
c -= 3; // Equivale a c - 3
console.log(c);

// Asignación de la multiplicación
let d = 125;
d *= 20; // Equivale a d * 20
console.log(d);

// Asignación con división
let e = 400;
e /= 2; // Equivale a e/2
console.log(e);

// Módulo de asignación
let f = 20;
f %= 3; // Equivale f%3
console.log(f);

// Exponenciación de asignación
let h = 12;
h **= 5; // Equivale a h**5
console.log(h);