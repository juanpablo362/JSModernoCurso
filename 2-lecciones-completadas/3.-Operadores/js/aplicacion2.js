// OPERADORES ARITMÉTICOS

let valor1 = 10;
let valor2 = 5;

// Suma
console.log(valor1 + valor2);

// Resta
console.log(valor1 - valor2);

// Multiplicación
console.log(valor1 * valor2);

// División
console.log(valor1/valor2);

// Módulo
console.log(valor1%valor2);

// Exponenciación
console.log(valor1 ** valor2);

// Operador de Incremento
let a = 2;
a++;
console.log(a);

// Operador de Decremento
let b = 10;
b--;
console.log(b);

